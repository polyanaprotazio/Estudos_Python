# if / elif      / else
# se / se não se / se não

entrada = input('Vc quer entrar ou sair? ')

if entrada == 'entrar': 
    print('Vc entrou no sistema.')
elif entrada == 'sair': 
    print('Vc saiu do sistema.')
elif entrada == 'Doido': 
    print('Vc ficou doido.')
else:
    print('Vc não digitou entrar ou sair.')
print('Agora acabou de vez')